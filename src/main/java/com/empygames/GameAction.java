package com.empygames;

public class GameAction {
	String id;
	String content;
	
	public GameAction(){
		
	}
	
	public void setID(String id){
		this.id = id;
	}
	
	public void setContent(String content){
		this.content = content;
	}
	
	public String getID(){
		return id;
	}
	
	public String getContent(){
		return content;
	}
}
